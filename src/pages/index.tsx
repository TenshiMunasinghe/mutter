import Head from "next/head";
import { Avatar, AvatarImage } from "~/@/components/ui/avatar";
import { Button } from "~/@/components/ui/button";
import { Form, FormControl, FormField, FormItem } from "~/@/components/ui/form";
import { Input } from "~/@/components/ui/input";
import Login from "~/components/Login";
import { useMakePost } from "~/components/MakePostModal";
import Post from "~/components/Post";
import { api } from "~/utils/api";

const Posts = () => {
  const { data } = api.post.getSome.useQuery();

  return (
    <div className="grid w-full space-y-[1px] border-x-[1px] border-gray-600 bg-gray-600">
      {data?.map((post) => (
        <Post key={post.id} id={post.id} />
      ))}
    </div>
  );
};

const MakePost = () => {
  const { form, onSubmit, user, isLoading } = useMakePost();

  if (!user) return null;

  return (
    <Form {...form}>
      <div className="sticky top-0 flex w-full items-stretch space-x-5 border-[1px] border-t-0 border-gray-600 bg-gray-950/50 p-5 backdrop-blur-sm">
        <Avatar className="h-8 w-8">
          <AvatarImage
            src={user.imageUrl}
            alt={`profile image of ${user.username || "someone"}`}
          />
        </Avatar>
        <form
          onSubmit={form.handleSubmit(onSubmit)}
          className="flex w-full flex-col space-y-4"
        >
          <FormField
            control={form.control}
            name="content"
            render={({ field }) => (
              <FormItem>
                <FormControl>
                  <Input
                    placeholder="Mutter something"
                    autoComplete="off"
                    className="w-full border-none"
                    {...field}
                  />
                </FormControl>
              </FormItem>
            )}
          />
          <Button type="submit" className="ml-auto" disabled={isLoading}>
            Mutter
          </Button>
        </form>
      </div>
    </Form>
  );
};

export default function Home() {
  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="relative mx-auto flex min-h-screen flex-col items-center lg:max-w-2xl">
        <MakePost />
        <Posts />
      </main>

      <Login />
    </>
  );
}
